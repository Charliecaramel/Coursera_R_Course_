# While Loops and Repeat Loops in R

# While loosp in R are essentially the same thing in other programms, it will repeat the specified command as long as
# the condtion specified in the While Loop remains true

#The general format of a While Loop is 

# while(condition specified){
#   While loop commands
#}

# A very simple example is as such

x = 0
while(x<101){
  print(x)
  x = x+1
}
